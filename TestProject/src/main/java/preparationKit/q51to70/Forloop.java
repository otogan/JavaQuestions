package preparationKit.q51to70;

public class Forloop {
    public static void main(String[] args) {

        int a=0;
        int b=0;

        for (int i = 0; i <10 ; i++) {
            System.out.print(++a);
        }

        System.out.println();

        for (int i = 0; i <10 ; i++) {
            System.out.print(b++);
        }

    }
}
